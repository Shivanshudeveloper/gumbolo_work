const mongoose = require('mongoose');
// Schema
const registerDataSchema = new mongoose.Schema({
    fname: {
        type: String,
        required: true
    },
    lname: {
        type: String,
        required: true
    },
    email: {
        type: String,
        required: true
    },
    password: {
        type: String,
        required: true
    },
    date: {
        type: Date,
        default: Date.now
    }
})
const register = mongoose.model('register', registerDataSchema)
module.exports = register